// OPERATORS IN JAVASCRIPT


// Arithmetic Operators
var a=110;
var b=10;
console.log("The value of a + b is " + (a + b));
console.log("The value of a - b is " + (a - b));
console.log("The value of a * b is " + (a * b));
console.log("The value of a / b is " + (a / b));    

// Assignment Operators
var c=b;
console.log("The value of c is " + c);
c += 2; // c = c + 2
console.log("The value of c after c += 2 is " + c);
c -= 2; // c = c - 2
console.log("The value of c after c -= 2 is " + c);
c *= 2; // c = c * 2
console.log("The value of c after c *= 2 is " + c);
c /= 2; // c = c / 2
console.log("The value of c after c /= 2 is " + c); 