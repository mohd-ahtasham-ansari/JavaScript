// OPERATORS IN JAVASCRIPT


// Arithmetic Operators
var a=110;
var b=10;
console.log("The value of a + b is " + (a + b));
console.log("The value of a - b is " + (a - b));
console.log("The value of a * b is " + (a * b));
console.log("The value of a / b is " + (a / b));    

// Assignment Operators
var c=b;
console.log("The value of c is " + c);
c += 2; // c = c + 2
console.log("The value of c after c += 2 is " + c);
c -= 2; // c = c - 2
console.log("The value of c after c -= 2 is " + c);
c *= 2; // c = c * 2
console.log("The value of c after c *= 2 is " + c);
c /= 2; // c = c / 2
console.log("The value of c after c /= 2 is " + c); 

//COMPARISON OPERATORS
var x=34;
var y = 56;
console.log(x == y);
console.log(x >= y);
console.log(x <= y);
console.log(x > y);
console.log(x < y);
console.log(x != y);

console.log("logical operators");
//LOGICAL OPERATORS

//logical AND
console.log(true && true);
console.log(true && false);
console.log(false && false);

//logical OR
console.log(true || true);
console.log(true || false);
console.log(false || false);        
 
//logical NOT
console.log(!true);
console.log(!false);